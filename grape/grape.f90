      SUBROUTINE INTERP
!
!        THIS SUBROUTINE STARTS WITH THE COARSE GRID SOLUTION
!        AND INTERPOLATES TO FIND THE STARTING SOLUTION FOR THE FINE
!        GRID.
!
      COMMON/GRIDA/X(170,85),Y(170,85)
!
      COMMON/GRIDB/DS(170),THETA(170),AAA(170),BBB(170),DSOB(170),      &
     &  THETOB(170),CCC(170),DDD(170),P1(170),Q1(170),R1(170),S1(170),  &
     &  OBANGS(170),DIST(241),AIRFX(241),AIRFY(241),CAMBRX(170),        &
     &  CAMBRY(170),XOB(170),YOB(170),XMIN(100),XMAX(100),YMIN(100),    &
     &  YMAX(100),JLE1A(2),JLE2A(2),NORDA(2),MAXITA(2),JCORN(4)
!
      COMMON/GRIDC/MAXIT,JS,JE,JINC,JTEBOT,JTETOP,KS,KE,KINC,JMAX,KMAX, &
     &  NTETYP,JPRT,NLETYP,JN1,JN2,NPLT,NIBDST,NOBSHP,NOBDST,NSEQ,      &
     &  JCAMBR,NAIRF,NORD,JAIRF,JDIST,NOUT,NDS,OMEGA,OMEGP,OMEGQ,       &
     &  OMEGR,OMEGS,TODEG,PLIM,QLIM,RLIM,SLIM,RADOB,XOBCNT,XLEFT,       &
     &  XRIGHT,YBOTOM,YTOP,RCORN,PI,TWOPI,PIO2,ALAMF,ALAMR,XLE,XTE,     &
     &  TEOPEN,BINN,TR,XTFRAC,ROTANG,ROTCTR,DSI,AAAI,BBBI,CCCI,DDDI,    &
     &  DSOBI,THETAI,THOBI,WAKEP,NOBCAS
!
      DIMENSION U(170,4),V(170),W(170,2),Z(170)
  REAL*8,DIMENSION(170):: dum
!
      KMM=KMAX-1
      J2=JS+JINC
      J3=JE-JINC
      J2P=J2+JINC
      J3M=J3-JINC
      JMM=JMAX-1
      JMM2=JMAX-2
!
!     INTERPOLATE X AND Y IN THE ETA(K) DIRECTION.
      K=0
      DO 1 KK=KS,KE,KINC
      K=K+1
    1 V(K)=REAL(KK)
!
      DO 2 K=2,KMM
    2 Z(K)=REAL(K)
!
      JF=JS
      JL=JE
      IF(NTETYP.LT.3)  GO TO 6
      JF=1
      JL=JMAX
!
    6 DO 3 J=JF,JL,JINC
      K=0
      DO 4 KK=KS,KE,KINC
      K=K+1
      U(K,1)=X(J,KK)
    4 U(K,2)=Y(J,KK)
      CALL CubicSpline(Z,W(1:,1),DUM,V,U(1:,1),2,KMM,1,K,0)
      CALL CubicSpline(Z,W(1:,2),DUM,V,U(1:,2),2,KMM,1,K,0)
      DO 5 K=2,KMM
      X(J,K)=W(K,1)
    5 Y(J,K)=W(K,2)
    3 END DO
!
!        INTERPOLATE X AND Y IN THE XI(J) DIRECTION.
      J=1
      DO 11 JJ=JS,JE,JINC
      J=J+1
   11 V(J)=REAL(JJ)
      V(1)=REAL(JS-JINC)
      J=J+1
      V(J)=REAL(JE+JINC)
!
      JF=1
      JL=JMAX
      IF(NTETYP.LT.3)  GO TO 7
      JF=2
      JL=JMM
    7 DO 12 J=JF,JL
   12 Z(J)=REAL(J)
!
      DO 13 K=2,KMM
      J=1
      DO 14 JJ=JS,JE,JINC
      J=J+1
      U(J,1)=X(JJ,K)
   14 U(J,2)=Y(JJ,K)
!
      IF(NTETYP.EQ.3)  GO TO 18
      U(1,1)=X(JE,K)
      U(1,2)=Y(JE,K)-TEOPEN
      J=J+1
      U(J,1)=X(JS,K)
      U(J,2)=Y(JS,K)+TEOPEN
      GO TO 19
   18 CONTINUE
      U(1,1)=X(1,K)
      U(1,2)=Y(1,K)
      J=J+1
      U(J,1)=X(JMAX,K)
      U(J,2)=Y(JMAX,K)
   19 CONTINUE
      CALL CubicSpline(Z,X(1:,K),DUM,V,U(1:,1),JF,JL,1,J,0)
      CALL CubicSpline(Z,Y(1:,K),DUM,V,U(1:,2),JF,JL,1,J,0)
   13 END DO
!
      IF(NTETYP.GT.1)  GO TO 30
!
!        INTERPOLATE P1, Q1, R1, AND S1 FOR THE CASE OF AN O-GRID
!        WITH A POINT AT THE TRAILING-EDGE.
      J=1
      DO 31 JJ=JS,JE,JINC
      J=J+1
      U(J,1)=P1(JJ)
      U(J,2)=Q1(JJ)
      U(J,3)=R1(JJ)
      U(J,4)=S1(JJ)
   31 V(J)=REAL(JJ)
!
      ONE3RD=1./3.
      U(1,1)=P1(3)+(P1(3)-P1(J2))*ONE3RD
      U(1,2)=Q1(3)+(Q1(3)-Q1(J2))*ONE3RD
      U(1,3)=R1(3)+(R1(3)-R1(J2))*ONE3RD
      U(1,4)=S1(3)+(S1(3)-S1(J2))*ONE3RD
      V(1)=1.
      J=J+1
      U(J,1)=P1(JE)+(P1(JE)-P1(J3))*ONE3RD
      U(J,2)=Q1(JE)+(Q1(JE)+Q1(J3))*ONE3RD
      U(J,3)=R1(JE)+(R1(JE)+R1(J3))*ONE3RD
      U(J,4)=S1(JE)+(S1(JE)+S1(J3))*ONE3RD
      V(J)=REAL(JMAX)
!
      CALL CubicSpline(Z,P1,DUM,V,U(1:,1),1,JMAX,1,J,0)
      CALL CubicSpline(Z,Q1,DUM,V,U(1:,2),1,JMAX,1,J,0)
      CALL CubicSpline(Z,R1,DUM,V,U(1:,3),1,JMAX,1,J,0)
      CALL CubicSpline(Z,S1,DUM,V,U(1:,4),1,JMAX,1,J,0)
      GO TO 21
!
!        INTERPOLATE P1, Q1, R1, AND S1 FOR THE CASE OF AN O-GRID
!        WITH NO POINT AT THE TRAILING EDGE.
   30 IF(NTETYP.EQ.3)  GO TO 20
      DVV=REAL((JMAX-5)*JINC)/REAL(JE-JS-4*JINC)
      VV=3.-DVV
      J=0
      DO 15 JJ=J2P,J3M,JINC
      J=J+1
      U(J,1)=P1(JJ)
      U(J,2)=Q1(JJ)
      U(J,3)=R1(JJ)
      U(J,4)=S1(JJ)
      VV=VV+DVV
   15 V(J)=VV
      P1(2)=P1(J2)
      P1(JMM)=P1(J3)
      Q1(2)=Q1(J2)
      Q1(JMM)=Q1(J3)
      R1(2)=R1(J2)
      R1(JMM)=R1(J3)
      S1(2)=S1(J2)
      S1(JMM)=S1(J3)
      ONE3RD=1./3.
      R1(1)=R1(J2)+ONE3RD*(R1(J3)-R1(J2))
      S1(1)=S1(J2)+ONE3RD*(S1(J3)-S1(J2))
      TWO3RD=2./3.
      R1(JMAX)=R1(J2)+TWO3RD*(R1(J3)-R1(J2))
      S1(JMAX)=S1(J2)+TWO3RD*(S1(J3)-S1(J2))
!
      CALL CubicSpline(Z,P1,DUM,V,U(1:,1),3,JMM2,1,J,0)
      CALL CubicSpline(Z,Q1,DUM,V,U(1:,2),3,JMM2,1,J,0)
      CALL CubicSpline(Z,R1,DUM,V,U(1:,3),3,JMM2,1,J,0)
      CALL CubicSpline(Z,S1,DUM,V,U(1:,4),3,JMM2,1,J,0)
      GO TO 21
!
!        INTERPOLATE P1, Q1, R1, AND S1 FOR THE CASE OF A C-GRID.
   20 J=1
      DO 22 JJ=JS,JE,JINC
      J=J+1
      U(J,1)=P1(JJ)
      U(J,2)=Q1(JJ)
      U(J,3)=R1(JJ)
   22 U(J,4)=S1(JJ)
!
      U(1,1)=-P1(J2)+2.*P1(JS)
      U(1,2)=-Q1(J2)+2.*Q1(JS)
      U(1,3)=-R1(J2)+2.*R1(JS)
      U(1,4)=-S1(J2)+2.*S1(JS)
      J=J+1
      U(J,1)=-P1(J3)+2.*P1(JE)
      U(J,2)=-Q1(J3)+2.*Q1(JE)
      U(J,3)=-R1(J3)+2.*R1(JE)
      U(J,4)=-S1(J3)+2.*S1(JE)
!
      CALL CubicSpline(Z,P1,DUM,V,U(1:,1),2,JMM,1,J,0)
      CALL CubicSpline(Z,Q1,DUM,V,U(1:,2),2,JMM,1,J,0)
      CALL CubicSpline(Z,R1,DUM,V,U(1:,3),2,JMM,1,J,0)
      CALL CubicSpline(Z,S1,DUM,V,U(1:,4),2,JMM,1,J,0)
!
   21 RETURN
      END Subroutine Interp

      SUBROUTINE OUTER
!
!        THIS SUBROUTINE COMPUTES THE LOCATIONS OF POINTS ON THE
!        OUTER BOUNDARY.  IT ALSO FILLS SOME ARRAYS WITH PARAMETERS
!        WHICH DETERMINE THE ANGLES AND SPACING AT THE OUTER BOUNDARY.
!
      COMMON/GRIDA/X(170,85),Y(170,85)
!
      COMMON/GRIDB/DS(170),THETA(170),AAA(170),BBB(170),DSOB(170),      &
     &  THETOB(170),CCC(170),DDD(170),P1(170),Q1(170),R1(170),S1(170),  &
     &  OBANGS(170),DIST(241),AIRFX(241),AIRFY(241),CAMBRX(170),        &
     &  CAMBRY(170),XOB(170),YOB(170),XMIN(100),XMAX(100),YMIN(100),    &
     &  YMAX(100),JLE1A(2),JLE2A(2),NORDA(2),MAXITA(2),JCORN(4)
!
      COMMON/GRIDC/MAXIT,JS,JE,JINC,JTEBOT,JTETOP,KS,KE,KINC,JMAX,KMAX, &
     &  NTETYP,JPRT,NLETYP,JN1,JN2,NPLT,NIBDST,NOBSHP,NOBDST,NSEQ,      &
     &  JCAMBR,NAIRF,NORD,JAIRF,JDIST,NOUT,NDS,OMEGA,OMEGP,OMEGQ,       &
     &  OMEGR,OMEGS,TODEG,PLIM,QLIM,RLIM,SLIM,RADOB,XOBCNT,XLEFT,       &
     &  XRIGHT,YBOTOM,YTOP,RCORN,PI,TWOPI,PIO2,ALAMF,ALAMR,XLE,XTE,     &
     &  TEOPEN,BINN,TR,XTFRAC,ROTANG,ROTCTR,DSI,AAAI,BBBI,CCCI,DDDI,    &
     &  DSOBI,THETAI,THOBI,WAKEP,NOBCAS
!
      DIMENSION PHA(9),SA(13)
!
      DATA N1/1/
!
!!!      YARC(YTARC,X)=YTARC-RARC+SQRT(RARC**2-(X-XARC)**2)
      YARC(YTARC,XXX)=YTARC-RARC+SQRT(RARC**2-(XXX-XARC)**2)
!
!        SPECIAL TREATMENT IF THE USER HAS READ IN HIS OWN OUTER
!        BOUNDARY.
      IF(NOBSHP.LT.6)  GO TO 95
      DO 94 J=1,JMAX
      X(J,KMAX)=XOB(J)
   94 Y(J,KMAX)=YOB(J)
      GO TO 11
!
!        COMPUTE LOCATIONS OF POINTS ON OUTER BOUNDARY FOR THE CASE
!        OF AN O-GRID AND THE OUTER BOUNDARY BEING A CIRCLE (NOBSHP=1).
   95 HTEOPN=.5*TEOPEN
      IF(NOBSHP.GT.1)  GO TO 2
      PHTEO=ASIN(HTEOPN/RADOB)
      DTH=(TWOPI-2.*PHTEO)/REAL(JMAX)
      TH=-DTH+PHTEO
      IF(NTETYP.GT.1)  TH=-DTH*.5+PHTEO
      TH=TH+ALAMR
      IF(JPRT.GE.0)  WRITE(6,101)
!
      DO 10 J=1,JMAX
      TH=TH+DTH
      PH=TH
      IF(NOBDST.EQ.3)  PH=OBANGS(J)
      X(J,KMAX)=RADOB*COS(PH)+XOBCNT
      Y(J,KMAX)=-RADOB*SIN(PH)
      IF(JPRT.LT.0)  GO TO 10
      PHD=PH*TODEG
      WRITE(6,100)  J,PHD,X(J,KMAX),Y(J,KMAX)
   10 END DO
      GO TO 11
!
!        PREPARE FOR COMPUTING RECTANGULAR OUTER BOUNDARIES.
    2 IF(NOBSHP.EQ.3.OR.NOBSHP.EQ.5)  GO TO 60
      YTMRC=YTOP-RCORN
      YBPRC=YBOTOM+RCORN
      XRMRC=XRIGHT-RCORN
      XLPRC=XLEFT+RCORN
      RRCORN=1.0/RCORN
      QCIRC=.5*PI*RCORN
      IF(NOBSHP.EQ.4)  GO TO 4
      IF(NOBDST.GT.1)  GO TO 12
!
!        COMPUTE LOCATIONS OF POINTS ON OUTER BOUNDARY FOR THE CASE
!        OF AN O-GRID, AND THE OUTER BOUNDARY BEING A RECTANGLE WITH
!        ROUNDED CORNERS, AND THE POINTS DISTRIBUTED BY EQUAL ARC-
!        LENGTH INCREMENTS (NOBSHP=2, NOBDST=1).
      STOTAL=2.*(XRIGHT-XLEFT)+2.*(YTOP-YBOTOM)-8.*RCORN+4.*QCIRC-TEOPEN
      DA=STOTAL/REAL(JMAX)
      SA(1)=-YBPRC-HTEOPN
      SA(2)=SA(1)+QCIRC
      SA(3)=SA(2)+XRMRC-XLPRC
      SA(4)=SA(3)+QCIRC
      SA(5)=SA(4)+YTMRC-YBPRC
      SA(6)=SA(5)+QCIRC
      SA(7)=SA(6)+XRMRC-XLPRC
      SA(8)=SA(7)+QCIRC
      SA(9)=STOTAL
      S=0.
      X(1,KMAX)=XRIGHT
      Y(1,KMAX)=0.-HTEOPN
      IF(NTETYP.EQ.0)  GO TO 16
      S=.5*DA
      Y(1,KMAX)=-S-HTEOPN
   16 KEY=1
      IF(JPRT.GE.0)  WRITE(6,105)
      IF(JPRT.GE.0)  WRITE(6,104) N1,KEY,S,X(1,KMAX),Y(1,KMAX)
!
      DO 15 J=2,JMAX
      S=S+DA
    9 IF(S.LE.SA(KEY))  GO TO 8
      KEY=KEY+1
      GO TO 9
!
    8 IF(KEY.GT.1)  CS=S-SA(KEY-1)
      GO TO (31,32,33,34,35,36,37,38,39),KEY
   31 X(J,KMAX)=XRIGHT
      Y(J,KMAX)=Y(J-1,KMAX)-DA
      GO TO 17
   32 PHIB=-CS*RRCORN
      X(J,KMAX)=XRMRC+RCORN*COS(PHIB)
      Y(J,KMAX)=YBPRC+RCORN*SIN(PHIB)
      GO TO 17
   33 X(J,KMAX)=XRMRC-CS
      Y(J,KMAX)=YBOTOM
      GO TO 17
   34 PHIB=-PIO2-CS*RRCORN
      X(J,KMAX)=XLPRC+RCORN*COS(PHIB)
      Y(J,KMAX)=YBPRC+RCORN*SIN(PHIB)
      GO TO 17
   35 X(J,KMAX)=XLEFT
      Y(J,KMAX)=YBPRC+CS
      GO TO 17
   36 PHIB=PI-CS*RRCORN
      X(J,KMAX)=XLPRC+RCORN*COS(PHIB)
      Y(J,KMAX)=YTMRC+RCORN*SIN(PHIB)
      GO TO 17
   37 X(J,KMAX)=XLPRC+CS
      Y(J,KMAX)=YTOP
      GO TO 17
   38 PHIB=PIO2-CS*RRCORN
      X(J,KMAX)=XRMRC+RCORN*COS(PHIB)
      Y(J,KMAX)=YTMRC+RCORN*SIN(PHIB)
      GO TO 17
   39 X(J,KMAX)=XRIGHT
      Y(J,KMAX)=YTMRC-CS
   17 CONTINUE
      IF(JPRT.LT.0)  GO TO 15
      WRITE(6,104)  J,KEY,S,X(J,KMAX),Y(J,KMAX)
   15 END DO
      GO TO 11
!
!        COMPUTE LOCATIONS OF POINTS ON OUTER BOUNDARY FOR THE CASE
!        OF AN O-GRID, AND THE OUTER BOUNDARY BEING A RECTANGLE WITH
!        ROUNDED CORNERS, AND THE POINTS DISTRIBUTED IN SOME ANGULAR
!        FASHION (NOBSHP=2, NOBDST=2 OR 3).
   12 PHTEO=ATAN2(HTEOPN,XRIGHT)
      DTH=TWOPI/REAL(JMAX)
      TH=-DTH+PHTEO
      IF(NTETYP.GT.1)  TH=-DTH*.5
      KEY=1
      PHA(1)=-ATAN2(YBPRC,XRIGHT)
      PHA(2)=-ATAN2(YBOTOM,XRMRC)
      PHA(3)=-ATAN2(YBOTOM,XLPRC)
      PHA(4)=-ATAN2(YBPRC,XLEFT)
      PHA(5)=TWOPI-ATAN2(YTMRC,XLEFT)
      PHA(6)=TWOPI-ATAN2(YTOP,XLPRC)
      PHA(7)=TWOPI-ATAN2(YTOP,XRMRC)
      PHA(8)=TWOPI-ATAN2(YTMRC,XRIGHT)
      PHA(9)=TWOPI
      IF(JPRT.GE.0)  WRITE(6,102)
!
      DO 13 J=1,JMAX
      TH=TH+DTH
      PH=TH
      IF(NOBDST.EQ.3)  PH=OBANGS(J)
   20 IF(PH.LE.PHA(KEY).OR.KEY.EQ.9)  GO TO 19
      KEY=KEY+1
      GO TO 20
!
   19 GO TO (21,22,23,24,25,26,27,28,29),KEY
   21 X(J,KMAX)=XRIGHT
      Y(J,KMAX)=-XRIGHT*TAN(PH)
      GO TO 14
   22 PHIB=-PH+ASIN((-XRMRC*TAN(PH)-YBPRC)*COS(PH)*RRCORN)
      X(J,KMAX)=XRMRC+RCORN*COS(PHIB)
      Y(J,KMAX)=YBPRC+RCORN*SIN(PHIB)
      GO TO 14
   23 X(J,KMAX)=-YBOTOM/TAN(PH)
      Y(J,KMAX)=YBOTOM
      GO TO 14
   24 PHIB=-PH+ASIN((-XLPRC*TAN(PH)-YBPRC)*COS(PH)*RRCORN)
      X(J,KMAX)=XLPRC+RCORN*COS(PHIB)
      Y(J,KMAX)=YBPRC+RCORN*SIN(PHIB)
      GO TO 14
   25 X(J,KMAX)=XLEFT
      Y(J,KMAX)=-XLEFT*TAN(PH)
      GO TO 14
   26 PHIB=-PH+ASIN((-XLPRC*TAN(PH)-YTMRC)*COS(PH)*RRCORN)
      X(J,KMAX)=XLPRC+RCORN*COS(PHIB)
      Y(J,KMAX)=YTMRC+RCORN*SIN(PHIB)
      GO TO 14
   27 X(J,KMAX)=-YTOP/TAN(PH)
      Y(J,KMAX)=YTOP
      GO TO 14
   28 PHIB=-PH+ASIN((-XRMRC*TAN(PH)-YTMRC)*COS(PH)*RRCORN)
      X(J,KMAX)=XRMRC+RCORN*COS(PHIB)
      Y(J,KMAX)=YTMRC+RCORN*SIN(PHIB)
      GO TO 14
   29 X(J,KMAX)=XRIGHT
      Y(J,KMAX)=-XRIGHT*TAN(PH)
   14 CONTINUE
      IF(JPRT.LT.0)  GO TO 13
      PHD=PH*TODEG
      WRITE(6,103)  J,KEY,PHD,X(J,KMAX),Y(J,KMAX)
   13 END DO
      GO TO 11
!
!        PREPARE FOR COMPUTING OUTER BOUNDARY FOR CASCADE GRIDS.
   60 XRMRC=XRIGHT-RCORN
      XLPRC=XLEFT+RCORN
      RRCORN=1./RCORN
      SALR=SIN(ALAMR)
      CALR=COS(ALAMR)
      TALR=TAN(ALAMR)
      SALF=SIN(ALAMF)
