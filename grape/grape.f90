SUBROUTINE CubicSplineScalar(xx,yy,yyp,x,y,n1,n2,j1,j2,kyyp)
! ---------------------------------------------------------------------------
! PURPOSE - Cubic spline interpolation
IMPLICIT DOUBLE PRECISION(A-H,O-Z)
IMPLICIT INTEGER(I-N)


  REAL*8,INTENT(IN):: xx
  REAL*8,INTENT(OUT):: yy
  REAL*8,INTENT(OUT):: yyp
  REAL*8,INTENT(IN),DIMENSION(:):: x,y
  INTEGER,INTENT(IN):: n1,n2
  INTEGER,INTENT(IN):: j1,j2
  INTEGER,INTENT(IN):: kyyp



!      DIMENSION XX(1),YY(1),YYP(1),X(1),Y(1)
!!!      COMMON/SCRACH/A(241),B(241),C(241),D(241),F(241),H(241)
  REAL*8,DIMENSION(241):: a,b,c,d,f,h
  REAL*8:: fudge
  INTEGER:: i,j,ja,jb
  REAL*8:: t,tt
  
!
!          CUBIC SPLINE INTERPOLATION
!  X,Y ARRAYS ARE TO BE INTERPOLATED
!  YY ARE FOUND INTERPOLATES CORRESPONDING TO XX
!  J1,,J2, ARE INDICE LIMITS ON X,Y
!  N1, N2 ARE INDICE LIMITS ON XX ( ALSO YY)
!  DIMENSION OF ARRAYS CARRIED IN FROM OUTSIDE, X(J) MUST BE MONOTONE
!  FORMULA FROM NUMERICAL METHODS BY DAHLQUIST,BJORCK,ANDERSON
!                                                 JLS FEB. 77
!        MODIFIED JUNE, 79 BY RLS.  THIS SUBROUTINE WAS SET UP TO
!        PRINT AN ERROR MESSAGE AND QUIT IF ANY ELEMENT OF XX WAS
!        OUTSIDE OF THE RANGE GIVEN BY X.  THE PROBLEM WAS THAT ROUND-
!        OFF COULD CAUSE THIS CONDITION TO INADVERTANTLY BE VIOLATED.
!        THE MODIFICATION WAS TO RELAX THE CONDITION SLIGHTLY, I.E.,
!        TO ALLOW A LITTLE BIT OF EXTRAPOLATION.
!
!        MODIFIED AGAIN OCTOBER, 79 BY RLS TO CALCULATE SLOPE OF YY.
!----------------------------------------------------------------------------
!      FIRST FIND DERIVATIVE LIKE TERMS THAT ARE COEFFICIENTS
      JA = J1 + 1
      JB = J2-1
      DO 10 J=JA,J2
      H(J) = X(J) - X(J-1)
   10 D(J) =(Y(J) - Y(J-1))/H(J)

      DO 12 J=JA,JB
      A(J) = H(J+1)
      B(J) = 2.*( H(J) + H(J+1))
   12 F(J) = 3.*( H(J)*D(J+1) + H(J+1)*D(J))

      B(J1) = 2.
      H(J1) = 1.
      F(J1) = 3.*D(JA)
      A(J2) = 1.
      B(J2) = 2.
      F(J2) = 3.*D(J2)
      CALL TRIB(A,B,H,C,F,J1,J2,1)
!
!  INTERPOLATION , X(J) ARRAY MUST BE MONOTONE
      FUDGE=1.E-10*ABS(X(J2)-X(J1))
      J = J1
      I = J1 + 1

!!!      DO 20 N=N1,N2
   28 IF(X(J)-FUDGE.LE.XX.AND.X(I)+FUDGE.GE.XX)  GO TO 30
      IF( X(I) - XX)22,22,24
   22 J = J +1
      I = I+1
      IF( I .GT. J2) GO TO 27
      GO TO 28
   24 J = J -1
      I = I-1
      IF( J.LT. J1) GO TO 27
      GO TO 28
   27 WRITE(6,600)
  600 FORMAT(55H0FATAL ERROR IN CSPLIN.  VALUE AT WHICH WE ARE SUPPOSED,&
     &  52H TO INTERPOLATE IS OUTSIDE OF GIVEN TABLE OF VALUES.)
      STOP
   30 T = (XX - X(J))/H(I)
      TT = 1. - T
      YY = T*Y(I) + TT*Y(J) + H(I)*T*TT*( (F(J) - D(I))*TT           &
     &   -(F(I)-D(I))*T)
      IF(KYYP.EQ.1)  YYP=F(J)+2.0*(-2.0*F(J)-F(I)+3.0*D(I))*T        &
     &  +3.*(F(J)+F(I)-2.0*D(I))*T**2
!!!   20 END DO

  RETURN
END Subroutine CubicSplineScalar   ! ----------------------------------------



