      SUBROUTINE CKSMTH(F,TOL,JMAX,J,KEY)
!
!        THIS SUBROUTINE CHECKS FOR SMOOTHNESS IN ARRAY F BY PASSING
!        A PARABOLA THROUGH THE NEAREST 3 POINTS AND OBSERVING
!        THE DIFFERENCE BETWEEN THE ACTUAL VALUE FOR F(J) AND THE
!        CORRESPONDING POINT ON THE PARABOLA.
!
!!!      DIMENSION F(4)
      REAL*8,INTENT(IN),DIMENSION(4):: f
      REAL*8,INTENT(IN):: tol
      INTEGER,INTENT(IN):: jmax
      INTEGER,INTENT(IN):: j
      INTEGER,INTENT(OUT):: key
!
      IF(J.GT.1)  GO TO 1
      F1=F(2)
      F2=F(3)
      F3=F(4)
      X1=2.
      X2=3.
      X3=4.
      XX=1.
      GO TO 2
!
    1 IF(J.GT.2)  GO TO 3
      F1=F(1)
      F2=F(3)
      F3=F(4)
      X1=1.
      X2=3.
      X3=4.
      XX=2.
      GO TO 2
!
    3 IF(J.LT.JMAX)  GO TO 4
      F1=F(JMAX-3)
      F2=F(JMAX-2)
      F3=F(JMAX-1)
      X1=1.
      X2=2.
      X3=3.
      XX=4.
!
    2 X1SQ=X1**2
      X2SQ=X2**2
      X3SQ=X3**2
      D1=X2SQ*X3-X3SQ*X2
      D2=X1SQ*X3-X3SQ*X1
      D3=X1SQ*X2-X2SQ*X1
      RD=1./(D1-D2+D3)
      A=X3*F2-X2*F3-X3*F1+X1*F3+X2*F1-X1*F2
      B=X2SQ*F3-X3SQ*F2-X1SQ*F3+X3SQ*F1+X1SQ*F2-X2SQ*F1
      C=F1*D1-F2*D2+F3*D3
      GO TO 5
!
    4 F1=F(J-2)
      F2=F(J-1)
      F3=F(J+1)
      XX=3.
!
      RD=-1./6.
      A=-2.*F1+3.*F2-F3
      B=12.*F1-15.*F2+3.*F3
      C=-16.*F1+12.*F2-2.*F3
!
    5 FF=(A*XX**2+B*XX+C)*RD
      DIF=TOL*ABS(FF)
      KEY=0
      IF(DIF.GT.0..AND.ABS(FF-F(J)).GT.DIF)  KEY=1
      RETURN
      END Subroutine Cksmth

