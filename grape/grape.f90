      GO TO 2
   25 XE=0.5*(X(J,K-2)-4.*X(J,K-1)+3.*X(J,K))
      YE=0.5*(Y(J,K-2)-4.*Y(J,K-1)+3.*Y(J,K))
!
    2 AJAC(K)=XX*YE-XE*YX
      IF(NOUT.EQ.2)  GO TO 24
!
      XYMETS(K,1)=XX
      XYMETS(K,2)=XE
      XYMETS(K,3)=YX
      XYMETS(K,4)=YE
   24 END DO
!
    1 IF(NOUT.EQ.0)  GO TO 3
      GO TO (11,12,13),NOUT
   11 WRITE(7)  (X(J,K),K=1,KMAX),(Y(J,K),K=1,KMAX)
      IF(J.EQ.JMAX)  WRITE(6,113)
      GO TO 3
   12 WRITE(7)  (X(J,K),K=1,KMAX),(Y(J,K),K=1,KMAX),(AJAC(K),K=1,KMAX)
      IF(J.EQ.JMAX)  WRITE(6,114)
      GO TO 3
   13 WRITE(7)  (X(J,K),K=1,KMAX),(Y(J,K),K=1,KMAX),(AJAC(K),K=1,KMAX), &
     &  ((XYMETS(K,L),K=1,KMAX),L=1,4)
      IF(J.EQ.JMAX)  WRITE(6,115)
!
    3 IF(JPRT.LE.0)  GO TO 26
      IF(((J-1)/JPRT)*JPRT.NE.J-1)  GO TO 26
!
      WRITE(6,100)  J
      WRITE(6,116)  (X(J,K),K=1,KMAX)
      WRITE(6,101)  J
      WRITE(6,116)  (Y(J,K),K=1,KMAX)
!
      IF(NOUT.LT.2)  GO TO 26
      WRITE(6,102)  J
      WRITE(6,116)  (AJAC(K),K=1,KMAX)
!
      IF(NOUT.EQ.2)  GO TO 26
      WRITE(6,103)  J
      WRITE(6,116)  (XYMETS(K,1),K=1,KMAX)
      WRITE(6,104)  J
      WRITE(6,116)  (XYMETS(K,2),K=1,KMAX)
      WRITE(6,105)  J
      WRITE(6,116)  (XYMETS(K,3),K=1,KMAX)
      WRITE(6,106)  J
      WRITE(6,116)  (XYMETS(K,4),K=1,KMAX)
   26 END DO
!
    4 RETURN
!
  100 FORMAT(8H0X AT J=,I3)
  101 FORMAT(8H0Y AT J=,I3)
  102 FORMAT(16H0JACOBIANS AT J=,I3)
  103 FORMAT(13H0DX/DXI AT J=,I3)
  104 FORMAT(14H0DX/DETA AT J=,I3)
  105 FORMAT(13H0DY/DXI AT J=,I3)
  106 FORMAT(14H0DY/DETA AT J=,I3)
  116 FORMAT(1X,8E15.6)
  112 FORMAT(1H1,50X,13HFINISHED GRID/)
  113 FORMAT(50H0SOLUTION VALUES FOR X AND Y HAVE BEEN WRITTEN ON ,     &
     &  7HUNIT 7.)
  114 FORMAT(40H0SOLUTION VALUES FOR X, Y, AND JACOBIAN ,               &
     &  28HHAVE BEEN WRITTEN ON UNIT 7.)
  115 FORMAT(50H0SOLUTION VALUES FOR X, Y, JACOBIAN, AND THE FOUR ,     &
     &  46HMETRIC QUANTITIES HAVE BEEN WRITTEN ON UNIT 7.)
      END Subroutine Output

      SUBROUTINE RELAX
!
!        THIS SUBROUTINE APPLIES AN S.L.O.R. TECHNIQUE TO SOLVE
!        THE POISSON EQUATIONS THAT GENERATE THE GRID.
!
      COMMON/GRIDA/X(170,85),Y(170,85)
!
      COMMON/GRIDB/DS(170),THETA(170),AAA(170),BBB(170),DSOB(170),      &
     &  THETOB(170),CCC(170),DDD(170),P1(170),Q1(170),R1(170),S1(170),  &
     &  OBANGS(170),DIST(241),AIRFX(241),AIRFY(241),CAMBRX(170),        &
     &  CAMBRY(170),XOB(170),YOB(170),XMIN(100),XMAX(100),YMIN(100),    &
     &  YMAX(100),JLE1A(2),JLE2A(2),NORDA(2),MAXITA(2),JCORN(4)
!
      COMMON/GRIDC/MAXIT,JS,JE,JINC,JTEBOT,JTETOP,KS,KE,KINC,JMAX,KMAX, &
     &  NTETYP,JPRT,NLETYP,JN1,JN2,NPLT,NIBDST,NOBSHP,NOBDST,NSEQ,      &
     &  JCAMBR,NAIRF,NORD,JAIRF,JDIST,NOUT,NDS,OMEGA,OMEGP,OMEGQ,       &
     &  OMEGR,OMEGS,TODEG,PLIM,QLIM,RLIM,SLIM,RADOB,XOBCNT,XLEFT,       &
     &  XRIGHT,YBOTOM,YTOP,RCORN,PI,TWOPI,PIO2,ALAMF,ALAMR,XLE,XTE,     &
     &  TEOPEN,BINN,TR,XTFRAC,ROTANG,ROTCTR,DSI,AAAI,BBBI,CCCI,DDDI,    &
     &  DSOBI,THETAI,THOBI,WAKEP,NOBCAS
!
      COMMON/SCRACH/RHS1(170),RHS2(170),RHS3(170),RHS4(170),PCORCT(170),&
     &  PRESID(170),QRESID(170),RRESID(170),DUMMY(86)
!
      DIMENSION A(170),B(170),C(170),D(170),F(170),H(170),JPPER(170),   &
     &  JMPER(170),XXB(170),YXB(170),XXXB(170),F0(170),F1(170),F2(170), &
     &  YXXB(170),XEB(170),YEB(170),XXC(170),YXC(170),XXXC(170),        &
     &  YXXC(170),XEC(170),YEC(170),F3(170),F4(170),F5(170),RJC(170),   &
     &  RJB(170),G(170),S(170),QPS(170),XF(170),YG(170),XC(170),        &
     &  RCORCT(170),SCORCT(170),XXD(170),YXD(170),XED(170),             &
     &  YED(170),AJSQ(170),AL(170),BE(170),GA(170),P(170),Q(170),R(170),&
     &  YC(170),SRESID(170),QCORCT(170)
!
      LOGICAL NVT,NVB
!
      EQUIVALENCE (RHS1(1),AJSQ(1)),(RHS2(1),AL(1)),(PCORCT(1),C(1)),   &
     &  (RHS3  (1),BE    (1),XF    (1)),(RHS4  (1),GA    (1),YG    (1)),&
     &  (PRESID(1),F     (1),XXD   (1)),(QRESID(1),G     (1),YXD   (1)),&
     &  (RRESID(1),A     (1),XED   (1)),(SRESID(1),B     (1),YED   (1)),&
     &  (QCORCT(1),P     (1),XC    (1)),(RCORCT(1),Q     (1),YC    (1)),&
     &  (SCORCT(1),R     (1),D     (1)),(S     (1),H     (1))
!
      DATA NCALL,NPANIC/0,0/
      DATA NLPP/60/
!
!        SET INDEX LIMITS, FACTORS FOR USE IN DIFFERENCE FORMULAS, ETC.
      NCALL=NCALL+1
      RDX=1./REAL(JINC)
      R2DX=.5*RDX
      RDXSQ=1./REAL(JINC)**2
      RDE=1./REAL(KINC)
      R2DE=.5*RDE
      RDESQ=1./REAL(KINC)**2
      RDXDE=1./(REAL(JINC)*REAL(KINC))
      TEORDX=TEOPEN*RDX
      TEO2DX=TEOPEN*R2DX
      TEODXS=TEOPEN*RDXSQ
      TALMR=TAN(ALAMR)
      CALMR=COS(ALAMR)
      SALMR=SIN(ALAMR)
      TERDXC=TEORDX*CALMR
      TERDXS=TEORDX*SALMR
      TE2DXS=TEO2DX*SALMR
      TE2DXC=TEO2DX*CALMR
      TEDXSC=TEODXS*CALMR
      TEDXSS=TEODXS*SALMR
      K2=KS+KINC
      K3=KS+2*KINC
      K4=KE-2*KINC
      K5=KE-KINC
      J2=JS+JINC
      J3=JE-JINC
      IF(NTETYP.NE.3.OR.NOBSHP.NE.6.AND.JTEBOT.NE.1)  GO TO 99
      NVB=(X(1,KMAX).NE.X(1,1))
      NVT=(X(JMAX,KMAX).NE.X(JMAX,1))
      IF(NVB)  SLABB=(Y(1,KMAX)-Y(1,1))/(X(1,KMAX)-X(1,1))
      IF(NVT)  SLABT=(Y(JMAX,KMAX)-Y(JMAX,1))/(X(JMAX,KMAX)-X(JMAX,1))
   99 CONTINUE
      JN1P=JN1+JINC
      JN1M=JN1-JINC
      JN2P=JN2+JINC
      ONE3RD=1./3.
      TWO3RD=2./3.
      NLPPM=NLPP-3
!
      DO 8 J=JS,JE,JINC
      JPPER(J)=J+JINC
    8 JMPER(J)=J-JINC
      IF(NTETYP.EQ.3)  GO TO 5
      JPPER(JE)=JS
      JMPER(JS)=JE
!
    5 IF(NTETYP.GT.1.OR.JINC.GT.1)  GO TO 74
      JPQF=J2
      JPQL=JE
      GO TO 12
   74 IF(NTETYP.EQ.3)  GO TO 75
      JPQF=J2
      JPQL=J3
      GO TO 12
   75 JPQF=JS
      JPQL=JE
!
!        COMPUTE DERIVATIVES OF X AND Y WITH RESPECT TO ETA AT INNER
!        AND OUTER BOUNDARIES.  THESE ARE FIXED FOR ALL ITERATIONS
!        AND HAVE EMBEDED IN THEM THE GIVEN REQUIREMENTS OF ANGLES AND
!        SPACING AT THOSE BOUNDARIES.
   12 DO 50 J=JS,JE,JINC
      JP=JPPER(J)
      JM=JMPER(J)
      XXB(J)=(X(JP,KS)-X(JM,KS))*R2DX
      YXB(J)=(Y(JP,KS)-Y(JM,KS))*R2DX
      XXC(J)=(X(JP,KE)-X(JM,KE))*R2DX
      YXC(J)=(Y(JP,KE)-Y(JM,KE))*R2DX
      XXXB(J)=(X(JP,KS)-2.*X(J,KS)+X(JM,KS))*RDXSQ
      YXXB(J)=(Y(JP,KS)-2.*Y(J,KS)+Y(JM,KS))*RDXSQ
      XXXC(J)=(X(JP,KE)-2.*X(J,KE)+X(JM,KE))*RDXSQ
      YXXC(J)=(Y(JP,KE)-2.*Y(J,KE)+Y(JM,KE))*RDXSQ
      IF(NTETYP.EQ.3.OR.J.NE.JS.AND.J.NE.JE)  GO TO 22
      XXB(J)=XXB(J)+TE2DXS
      YXB(J)=YXB(J)+TE2DXC
      XXC(J)=XXC(J)+TE2DXS
      YXC(J)=YXC(J)+TE2DXC
   20 IF(J.EQ.JE)  GO TO 21
      XXXB(J)=XXXB(J)-TEDXSS
      YXXB(J)=YXXB(J)-TEDXSC
      XXXC(J)=XXXC(J)-TEDXSS
      YXXC(J)=YXXC(J)-TEDXSC
      GO TO 22
   21 XXXB(J)=XXXB(J)+TEDXSS
      YXXB(J)=YXXB(J)+TEDXSC
      XXXC(J)=XXXC(J)+TEDXSS
      YXXC(J)=YXXC(J)+TEDXSC
   22 STH=SIN(THETA(J))
      CTH=COS(THETA(J))
      DUM=DS(J)/SQRT(XXB(J)**2+YXB(J)**2)
      XEB(J)=DUM*(-XXB(J)*CTH-YXB(J)*STH)
      YEB(J)=DUM*(-YXB(J)*CTH+XXB(J)*STH)
      STH=SIN(THETOB(J))
      CTH=COS(THETOB(J))
      DUM=DSOB(J)/SQRT(XXC(J)**2+YXC(J)**2)
      XEC(J)=DUM*(-XXC(J)*CTH-YXC(J)*STH)
      YEC(J)=DUM*(-YXC(J)*CTH+XXC(J)*STH)
   50 END DO
!
!        INSERT REAR BOUNDARY CONDITIONS FOR THE CASE OF A C-GRID.
      IF(NTETYP.LT.3)  GO TO 13
      ATABB=ATAN2(X(1,KMAX)-X(1,1),Y(1,KMAX)-Y(1,1))
      SABB=SIN(ATABB)
      CABB=COS(ATABB)
      XEB(1)=-DS(1)*SABB
      YEB(1)=-DS(1)*CABB
      XEC(1)=DSOB(1)*SABB
      YEC(1)=DSOB(1)*CABB
      ATABT=ATAN2(X(JMAX,KMAX)-X(JMAX,1),Y(JMAX,KMAX)-Y(JMAX,1))
      SABT=SIN(ATABT)
      CABT=COS(ATABT)
      XEB(JMAX)=-DS(JMAX)*SABT
      YEB(JMAX)=-DS(JMAX)*CABT
      XEC(JMAX)=DSOB(JMAX)*SABT
      YEC(JMAX)=DSOB(JMAX)*CABT
!
!        DIFFERENCE WITH RESPECT TO XI TO GET MIXED SECOND PARTIALS
!        OF X AND Y AT INNER AND OUTER BOUNDARIES.  STORE FACTORS
!        FOR USE IN COMPUTING P1, Q1, R1, AND S1 FOR EACH ITERATION.
   13 DO 1 J=JS,JE,JINC
      JP=JPPER(J)
      JM=JMPER(J)
      XXEB=(XEB(JP)-XEB(JM))*R2DX
      YXEB=(YEB(JP)-YEB(JM))*R2DX
      XXEC=(XEC(JP)-XEC(JM))*R2DX
      YXEC=(YEC(JP)-YEC(JM))*R2DX
      RJB(J)=1.0/(XXB(J)*YEB(J)-XEB(J)*YXB(J))
      RJSQ=RJB(J)**2
      AD=XEB(J)**2+YEB(J)**2
      BD=XXB(J)*XEB(J)+YXB(J)*YEB(J)
      GD=XXB(J)**2+YXB(J)**2
      F0(J)=-GD*RJSQ
      F1(J)=(-AD*XXXB(J)+2.0*BD*XXEB)*RJSQ
      F2(J)=(-AD*YXXB(J)+2.0*BD*YXEB)*RJSQ
      RJC(J)=1.0/(XXC(J)*YEC(J)-XEC(J)*YXC(J))
      RJSQ=RJC(J)**2
      AD=XEC(J)**2+YEC(J)**2
      BD=XXC(J)*XEC(J)+YXC(J)*YEC(J)
      GD=XXC(J)**2+YXC(J)**2
      F3(J)=-GD*RJSQ
      F4(J)=(-AD*XXXC(J)+2.0*BD*XXEC)*RJSQ
      F5(J)=(-AD*YXXC(J)+2.0*BD*YXEC)*RJSQ
    1 END DO
!
!        *************************************************************
!        *                                                           *
!        *              START OF THE MAIN ITERATION LOOP             *
!        *                                                           *
!        *************************************************************
!
!        INSIDE OF THE MAIN ITERATION LOOP THERE ARE MANY SMALL LOOPS
!        WITH J AS THE INDEX.  THE CODE WAS WRITTEN THIS WAY TO
!        ALLOW AS MANY AS POSSIBLE OF THOSE LOOPS TO BE INSTACK ON THE
!        CDC 7600.  ON OTHER COMPUTERS IT MIGHT BE DESIRABLE TO
!        SIMPLIFY THE CODE BY CONSOLIDATING MANY OF THOSE LOOPS.
   29 CONTINUE
      DO 23 ITER=1,MAXIT
      CMAX=0.
      CSUM=0.
      Q1SUM=0.0
      P1CM=0.
      Q1CM=0.
      R1CM=0.
      S1CM=0.
      P1M=0.
      Q1M=0.
      R1M=0.
      S1M=0.
!
!        COMPUTE SECOND DERIVATIVES OF X AND Y WITH RESPECT TO ETA,
!        FROM WHICH P1, Q1, R1, AND S1 AT THIS ITERATION ARE COMPUTED.
!        APPLY THE LIMITING ALGORITHM TO COMBAT THE TENDANCY TOWARD
!        INSTABILITY IN THE FIRST FEW ITERATIONS.  ALSO COMPUTE MAXIMA
!        OF THOSE FUNCTIONS FOR USE ON THE PRINTOUT.
      DO 63 J=JPQF,JPQL,JINC
      XEEB=.5*RDESQ*(-7.*X(J,KS)+8.*X(J,K2)-X(J,K3))-3.*RDE*XEB(J)
   63 RHS1(J)=F1(J)+F0(J)*XEEB
      DO 52 J=JPQF,JPQL,JINC
      YEEB=.5*RDESQ*(-7.*Y(J,KS)+8.*Y(J,K2)-Y(J,K3))-3.*RDE*YEB(J)
   52 RHS2(J)=F2(J)+F0(J)*YEEB
      DO 72 J=JPQF,JPQL,JINC
   72 PRESID(J)=(YEB(J)*RHS1(J)-XEB(J)*RHS2(J))*RJB(J)-P1(J)
      DO 30 J=JPQF,JPQL,JINC
   30 QRESID(J)=(-YXB(J)*RHS1(J)+XXB(J)*RHS2(J))*RJB(J)-Q1(J)
      DO 70 J=JPQF,JPQL,JINC
      PJUMP=PLIM*MAX(ABS(P1(J)),1.0)
   70 PCORCT(J)=SIGN(MIN(OMEGP*ABS(PRESID(J)),PJUMP),PRESID(J))
      DO 31 J=JPQF,JPQL,JINC
      QJUMP=QLIM*MAX(ABS(Q1(J)),1.0)
   31 QCORCT(J)=SIGN(MIN(OMEGQ*ABS(QRESID(J)),QJUMP),QRESID(J))
      DO 64 J=JS,JE,JINC
      XEEC=.5*RDESQ*(-7.*X(J,KE)+8.*X(J,K5)-X(J,K4))+3.*RDE*XEC(J)
   64 RHS3(J)=F4(J)+F3(J)*XEEC
      DO 32 J=JS,JE,JINC
      YEEC=.5*RDESQ*(-7.*Y(J,KE)+8.*Y(J,K5)-Y(J,K4))+3.*RDE*YEC(J)
   32 RHS4(J)=F5(J)+F3(J)*YEEC
      DO 73 J=JS,JE,JINC
   73 RRESID(J)=(YEC(J)*RHS3(J)-XEC(J)*RHS4(J))*RJC(J)-R1(J)
      DO 33 J=JS,JE,JINC
   33 SRESID(J)=(-YXC(J)*RHS3(J)+XXC(J)*RHS4(J))*RJC(J)-S1(J)
      DO 35 J=JS,JE,JINC
      RJUMP=RLIM*MAX(ABS(R1(J)),1.0)
   35 RCORCT(J)=SIGN(MIN(OMEGR*ABS(RRESID(J)),RJUMP),RRESID(J))
      DO 71 J=JS,JE,JINC
      SJUMP=SLIM*MAX(ABS(S1(J)),1.0)
   71 SCORCT(J)=SIGN(MIN(OMEGS*ABS(SRESID(J)),SJUMP),SRESID(J))
      DO 37 J=JPQF,JPQL,JINC
      P1(J)=P1(J)+PCORCT(J)
      IF(JPRT.LT.0)  GO TO 37
      ABSPC=ABS(PCORCT(J))
      IF(ABSPC.LT.P1CM)  GO TO 36
      P1CM=ABSPC
   36 ABSP1=ABS(P1(J))
      IF(ABSP1.LT.P1M)  GO TO 37
      P1M=ABSP1
      JP1M=J
   37 END DO
      DO 39 J=JPQF,JPQL,JINC
      Q1(J)=Q1(J)+QCORCT(J)
      IF(JPRT.LT.0)  GO TO 39
      ABSQC=ABS(QCORCT(J))
      IF(ABSQC.LT.Q1CM)  GO TO 38
      Q1CM=ABSQC
   38 ABSQ1=ABS(Q1(J))
      IF(ABSQ1.LT.Q1M)  GO TO 39
      Q1M=ABSQ1
      JQ1M=J
   39 Q1SUM=Q1SUM+Q1(J)
      DO 41 J=JS,JE,JINC
      R1(J)=R1(J)+RCORCT(J)
      IF(JPRT.LT.0)  GO TO 41
      ABSRC=ABS(RCORCT(J))
      IF(ABSRC.LT.R1CM)  GO TO 40
      R1CM=ABSRC
   40 ABSR1=ABS(R1(J))
      IF(ABSR1.LT.R1M)  GO TO 41
      R1M=ABSR1
      JR1M=J
   41 END DO
      DO 43 J=JS,JE,JINC
      S1(J)=S1(J)+SCORCT(J)
      IF(JPRT.LT.0)  GO TO 43
      ABSSC=ABS(SCORCT(J))
      IF(ABSSC.LT.S1CM)  GO TO 42
      S1CM=ABSSC
   42 ABSS1=ABS(S1(J))
      IF(ABSS1.LT.S1M)  GO TO 43
      S1M=ABSS1
      JS1M=J
   43 END DO
!
!        IN THE CASE OF AN O-GRID WITH A SHARP TRAILING EDGE AND A
!        GRID POINT AT THAT TRAILING EDGE AND THE FINE-MESH SOLUTION,
!        COMPROMISE P1 AND Q1 AT THE TRAILING EDGE TO FACILITATE
!        CONVERGENCE.
      IF(NTETYP.NE.1.OR.JINC.NE.1)  GO TO 3
      P1(1)=0.5*(P1(2)+P1(JMAX))
      Q1(1)=0.5*(P1(2)+Q1(JMAX))
      GO TO 14
!
!        FOR ALL OTHER O-GRID CASES, COMPROMISE P1 AND Q1 AT THE
!        TRAILING EDGE TO FACILITATE CONVERGENCE.
    3 IF(NTETYP.EQ.3)  GO TO 14
      PDIF=P1(J2)-P1(J3)
      P1(JS)=P1(J3)+PDIF*TWO3RD
      P1(JE)=P1(J3)+PDIF*ONE3RD
      QDIF=Q1(J2)-Q1(J3)
      Q1(JS)=Q1(J3)+QDIF*TWO3RD
      Q1(JE)=Q1(J3)+QDIF*ONE3RD
!
!        COMPROMISE P1 AND Q1 AT LEADING EDGE TO FACILITATE CONVERGENCE
!        IF LEADING EDGE IS SHARP.
   14 GO TO (9,18,19),NLETYP
   18 P1(JN1)=.5*(P1(JN1M)+P1(JN1P))
      Q1(JN1)=.5*(Q1(JN1M)+Q1(JN1P))
      GO TO 9
   19 PDIF=P1(JN2P)-P1(JN1M)
      P1(JN1)=P1(JN1M)+PDIF*ONE3RD
      P1(JN2)=P1(JN1M)+PDIF*TWO3RD
      QDIF=Q1(JN2P)-Q1(JN1M)
      Q1(JN1)=Q1(JN1M)+QDIF*ONE3RD
      Q1(JN2)=Q1(JN1M)+QDIF*TWO3RD
    9 CONTINUE
!
!        LOOP ON K.  DIRECTION VARRIES WITH SIGN OF Q1SUM.
      DO 10 KK=K2,K5,KINC
      IF(Q1SUM.GT.0.0)  GO TO 26
      K=KK
      GO TO 27
   26 K=KE+KS-KK
   27 KP=K+KINC
      KM=K-KINC
!
!        FILL TRIDIAGONAL ARRAYS.
      DO 65 J=JS,JE,JINC
      JP=JPPER(J)
      JM=JMPER(J)
      XXD(J)=(X(JP,K)-X(JM,K))*R2DX
   65 YXD(J)=(Y(JP,K)-Y(JM,K))*R2DX
      XXD(JS)=XXD(JS)+TE2DXS
      YXD(JS)=YXD(JS)+TE2DXC
      XXD(JE)=XXD(JE)+TE2DXS
      YXD(JE)=YXD(JE)+TE2DXC
      DO 11 J=JS,JE,JINC
      XED(J)=(X(J,KP)-X(J,KM))*R2DE
   11 YED(J)=(Y(J,KP)-Y(J,KM))*R2DE
      DO 45 J=JS,JE,JINC
      AJSQ(J)=(XXD(J)*YED(J)-XED(J)*YXD(J))**2
      AL(J)=XED(J)**2+YED(J)**2
      BE(J)=XXD(J)*XED(J)+YXD(J)*YED(J)
   45 GA(J)=XXD(J)**2+YXD(J)**2
      DO 66 J=JS,JE,JINC
      JP=JPPER(J)
      JM=JMPER(J)
      XXED=.25*RDXDE*(X(JP,KP)-X(JP,KM)-X(JM,KP)+X(JM,KM))
   66 F(J)=2.*BE(J)*XXED
      DO 46 J=JS,JE,JINC
      JP=JPPER(J)
      JM=JMPER(J)
      YXED=.25*RDXDE*(Y(JP,KP)-Y(JP,KM)-Y(JM,KP)+Y(JM,KM))
   46 G(J)=2.*BE(J)*YXED
      DO 69 J=JS,JE,JINC
      F(J)=F(J)-GA(J)*RDESQ*(X(J,KP)+X(J,KM))
   69 G(J)=G(J)-GA(J)*RDESQ*(Y(J,KP)+Y(J,KM))
      DO 47 J=JS,JE,JINC
      A(J)=AL(J)*RDXSQ
      B(J)=-2.*AL(J)*RDXSQ-2.*GA(J)*RDESQ
   47 C(J)=AL(J)*RDXSQ
