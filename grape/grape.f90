      IF(ANGA.LT.0.0)  ANGA=ANGA+360.
      DELS=SQRT((X(J,K2)-X(J,KS))**2+(Y(J,K2)-Y(J,KS))**2)
   97 WRITE(6,101)  J,ANGA,ANG1,DELS,X(J,KS),Y(J,KS),X(J,K2),Y(J,K2),   &
