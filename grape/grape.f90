      DO 1 K=K2,KE,KINC
      KK=K-KINC
    1 DISTIC(K)=DISTIC(KK)+DSM*(1.+EPS)**(K-KS-KINC)
      RLAST=1./DISTIC(KE)
      IF(NCALL.EQ.1.AND.JPRT.GE.0)  WRITE(6,100)
