      SUBROUTINE IC
!
!        THIS SUBROUTINE FILLS THE X, Y, P1, Q1, R1, AND S1 ARRAYS
!        WITH INITIAL CONDITIONS.  X AND Y ARE FILLED USING A
!        PRE-DETERMINED EXPONENTIAL STRECHING.
