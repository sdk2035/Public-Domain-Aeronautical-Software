      SUBROUTINE TRIB(A,B,C,X,F,NL,NU,JINC)
      DIMENSION A(2),B(2),C(2),X(2),F(2)
!
!        THIS SUBROUTINE SOLVES A TRI-DIAGONAL SYSTEM OF LINEAR
!        EQUATIONS.
!
      X(NL)=C(NL)/B(NL)
      F(NL)=F(NL)/B(NL)
      NLP1=NL+JINC
      DO 1 J=NLP1,NU,JINC
      JM=J-JINC
      Z=1./(B(J)-A(J)*X(JM))
      X(J)=C(J)*Z
    1 F(J)=(F(J)-A(J)*F(JM))*Z
      NUPNL=NU+NL
      DO 2 J1=NLP1,NU,JINC
      J=NUPNL-J1
      JP=J+JINC
    2 F(J)=F(J)-X(J)*F(JP)
      RETURN
      END Subroutine Trib

      SUBROUTINE TRIP(A,B,C,F,Q,S,J1,J2,JINC)
      REAL*8,INTENT(IN),DIMENSION(3):: a,b,c
      REAL*8,INTENT(IN OUT),DIMENSION(3):: f
      REAL*8,INTENT(OUT),DIMENSION(3):: q,s
      INTEGER,INTENT(IN):: j1,j2,jinc

!      DIMENSION A(3),B(3),C(3),F(3),Q(3),S(3)
      JA=J1+JINC
      FN = F(J2)
!   FORWARD ELIMINATION SWEEP
      Q(J1) = -C(J1)/B(J1)
      F(J1) = F(J1)/B(J1)
      S(J1) = - A(J1)/B(J1)
      DO 10 J=JA,J2,JINC
      JM=J-JINC
      P=1./(B(J)+A(J)*Q(JM))
      Q(J) = - C(J)*P
      F(J)=(F(J)-A(J)*F(JM))*P
      S(J)=-A(J)*S(JM)*P
   10 END DO
!   BACKWARD PASS
      JJ = J1 + J2
      Q(J2) = 0.
      S(J2) = 1.
      DO 11 I=JA,J2,JINC
      J = JJ - I
      JP=J+JINC
      S(J)=S(J)+Q(J)*S(JP)
   11 Q(J)=F(J)+Q(J)*Q(JP)
      J2M=J2-JINC
      F(J2)=(FN-C(J2)*Q(J1)-A(J2)*Q(J2M))/(C(J2)*S(J1)+A(J2)*S(J2M)     &
     &  +B(J2))
!    BACKWARD ELIMINATION PASS
      DO 12 I=JA,J2,JINC
      J = JJ -I
   12 F(J) = F(J2)*S(J) + Q(J)
      RETURN
      END Subroutine Trip

