SUBROUTINE TRIB(A,B,C,X,F,NL,NU,JINC)
IMPLICIT NONE
  REAL*8,INTENT(IN),DIMENSION(:):: a,b,c
  REAL*8,INTENT(OUT),DIMENSION(:):: x,f
  INTEGER,INTENT(IN):: nl,nu
  INTEGER,INTENT(IN):: jinc

  INTEGER:: j,j1,jm,jp
  INTEGER:: nlp1
  INTEGER:: nupnl
  REAL:: z
!!!      DIMENSION A(2),B(2),C(2),X(2),F(2)
!
!        THIS SUBROUTINE SOLVES A TRI-DIAGONAL SYSTEM OF LINEAR
!        EQUATIONS.
!
      X(NL)=C(NL)/B(NL)
      F(NL)=F(NL)/B(NL)
      NLP1=NL+JINC
      DO 1 J=NLP1,NU,JINC
      JM=J-JINC
      Z=1./(B(J)-A(J)*X(JM))
      X(J)=C(J)*Z
    1 F(J)=(F(J)-A(J)*F(JM))*Z

      NUPNL=NU+NL
      DO 2 J1=NLP1,NU,JINC
      J=NUPNL-J1
      JP=J+JINC
    2 F(J)=F(J)-X(J)*F(JP)

      RETURN
      END Subroutine Trib


