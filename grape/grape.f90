      COMMON/GRIDC/MAXIT,JS,JE,JINC,JTEBOT,JTETOP,KS,KE,KINC,JMAX,KMAX, &
     &  NTETYP,JPRT,NLETYP,JN1,JN2,NPLT,NIBDST,NOBSHP,NOBDST,NSEQ,      &
