  102 FORMAT(49H0FATAL ERROR IN INNER.  FAILURE TO CONVERGE.  EH=,      &
     &  F6.1,5X,2HI=,I4,5X,2HF=,F15.12)
  110 FORMAT(8H0TEOPEN=,F13.6)
      END Subroutine Inner

