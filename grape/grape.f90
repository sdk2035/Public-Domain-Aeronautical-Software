      DO 3 J=JF,JL,JINC
      DX=X(J,KE)-X(J,KS)
      DY=Y(J,KE)-Y(J,KS)
      P1(J)=0.
      Q1(J)=0.
      R1(J)=0.
      S1(J)=0.
!
      DO 3 K=K2,K3,KINC
      X(J,K)=X(J,KS)+DISTIC(K)*DX
    3 Y(J,K)=Y(J,KS)+DISTIC(K)*DY
      RETURN
  100 FORMAT(54H0INITIAL CONDITIONS WILL BE ACCORDING TO THE FOLLOWING, &
     &  23H EXPONENTIAL STRECHING:/3X,1HK,9X,6HDISTIC)
  101 FORMAT(1X,I3,5X,F10.6)
      END Subroutine IC

